package mag;

public class MagicS {
	
	protected int idS;
	protected String nameS;
	
	public int getIdS() {
		return idS;
	}
	public void setIdS(int idS) {
		this.idS = idS;
	}
	public String getNameS() {
		return nameS;
	}
	public void setNameS(String nameS) {
		this.nameS = nameS;
	}
	@Override
	public String toString() {
		return "MagicS [idS=" + idS + ", nameS=" + nameS + "]";
	}
	

}